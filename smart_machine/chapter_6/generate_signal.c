// 生成方波
void generateSquareWave() {
    DAC0832 = 0; // 设置DAC值为0（低电平）
    Delay5ms(); // 延时
    DAC0832 = 255; // 设置DAC值为255（高电平）
    Delay5ms(); // 延时
}

// 生成正向锯齿波
void generateUpSawtoothWave() {
    for (uint8_t i = 0; i <= 255; i++) {
        DAC0832 = i;
        Delay5ms(); // 延时
    }
}

// 生成反向锯齿波
void generateDownSawtoothWave() {
    for (uint8_t i = 255; i > 0; i--) {
        DAC0832 = i;
        Delay5ms(); // 延时
    }
}

// 生成三角波
void generateTriangleWave() {
    for (uint8_t i = 0; i < 255; i++) {
        DAC0832 = i;
        Delay5ms(); // 延时
    }
    for (uint8_t i = 255; i > 0; i--) {
        DAC0832 = i;
        Delay5ms(); // 延时
    }
}

// 生成正弦波函数
void generateSineWave() {
    for (uint8_t i = 0; i < 256; i++) { // 遍历查找表中的每个元素
        DAC0832 = sin_table[i]; // 直接从查找表中获取正弦值并设置DAC0832
        Delay5ms(1); // 延时
    }
}